# oprator penugasan
x = 5
x += 10

# oprator logika
benar = True
salah = False
print(benar and salah)
print(benar or salah)
print(not benar)

# oprator bitwise
x = 5
y = 3
print(x & y)
print(x | y)

# oprator identitas
a = [1,2,3]
b = a
c = [4,5,6]

print(a is b)
print(a is c)

# opratpr keanggotaan
fruits = ["apple","banana", "cherry"]

print("apple" in fruits)
print("grape" not in fruits)